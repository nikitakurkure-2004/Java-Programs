//write a program to print the array in reverse order
class PrintReverseArrray
{
      public static void main(String[] args)
          {
                int[] arr={10, 30, 20, 60, 80, 90};
                for(int i = arr.length-1; i>=0; i--)
                {
                      System.out.println(arr[i]);
                }
          }
}
              